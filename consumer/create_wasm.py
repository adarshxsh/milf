wasm_bytes = bytes([
    0x00, 0x61, 0x73, 0x6d, 0x01, 0x00, 0x00, 0x00, # Magic & Version
    0x01, 0x05, 0x01, 0x60, 0x00, 0x01, 0x7f,       # Type: () -> (i32)
    0x03, 0x02, 0x01, 0x00,                         # Function: [0]
    0x07, 0x08, 0x01, 0x04, 0x6d, 0x61, 0x69, 0x6e, 0x00, 0x00, # Export: "main" -> func 0
    0x0a, 0x06, 0x01, 0x04, 0x00, 0x41, 0x38, 0x0b  # Code: i32.const 56, end
])

with open('simple.wasm', 'wb') as f:
    f.write(wasm_bytes)

print("simple.wasm created successfully.")
